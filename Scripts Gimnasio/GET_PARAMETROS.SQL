CREATE PROCEDURE CARGAR_PARAMETROS (
    DESCUENTO INTEGER,
    PORCENTAJE INTEGER,
    CUMPLE INTEGER)
AS
BEGIN
  UPDATE PARAMETROS SET VALOR=:DESCUENTO WHERE ID_PARAMETRO=1;
  UPDATE PARAMETROS SET VALOR=:PORCENTAJE WHERE ID_PARAMETRO=2;
  UPDATE PARAMETROS SET VALOR=:CUMPLE WHERE ID_PARAMETRO=3;
  SUSPEND;
END !!

SET TERM ;!!

